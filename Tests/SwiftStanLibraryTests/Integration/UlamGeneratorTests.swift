  /// Minimal CSV parser tailored to McElreath's chimpanzees.csv:
  /// `;`-separated, quoted headers, drops the `recipient` column (which
  /// holds `NA`). Returns one Int column per kept header.
  private static func parseChimpanzeesCsv(at path: String) throws -> [String: [Int]] {
    let raw = try String(contentsOfFile: path, encoding: .utf8)
    let lines = raw.split(whereSeparator: \.isNewline).map(String.init)
    let quotes = CharacterSet(charactersIn: "\"")
    let headers = lines[0].split(separator: ",").map {
      $0.trimmingCharacters(in: quotes)
    }
    let keepIndices = headers.enumerated()
      .filter { $0.element != "recipient" }
      .map { $0.offset }
    var columns: [String: [Int]] = [:]
    for i in keepIndices { columns[headers[i]] = [] }
    for row in lines.dropFirst() {
      let values = row.split(separator: ",", omittingEmptySubsequences: false).map(String.init)
      for i in keepIndices {
        guard let v = Int(values[i].trimmingCharacters(in: quotes)) else {
          throw ChimpanzeesParseError.nonInteger(column: headers[i], value: values[i])
        }
        columns[headers[i]]!.append(v)
      }
    }
    return columns
  }

  enum ChimpanzeesParseError: Error {
    case nonInteger(column: String, value: String)
  }

