  /// Index columns must NOT be promoted even if they appear in
  /// arithmetic — Stan requires `array[N] int` for array subscripts.
  /// This is a defensive test: under normal use, an index column
  /// referenced as `a[group]` wouldn't surface as a multiplicative
  /// operand, but if a contrived RHS did both (e.g. `a[group] + 2*group`)
  /// the index-column declaration must win.
  @Test func indexColumnsAreNotAutoPromotedEvenWhenMultiplicativeOperand() throws {
    let data: UlamData = [
      "y":     .integer([0, 1, 0, 1]),
      "x":     .real([0.1, 0.2, 0.3, 0.4]),
      "group": .integer([1, 2, 1, 2]),
    ]
    let model = UlamModel(data: data) {
